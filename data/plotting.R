oxygen_average <- read.csv(file='average.csv', sep=',', header=FALSE)
A <- seq(from = 0, to = (72*5), by = 5)
A <- (A/60)
matplot(A, oxygen_average, type = "l", pch=1, lty=1, col = 1:6, axes=F, ann=F, ylim=c(0,25))
#legend("topright", legend = 1:24, col=1:6, pch=1)
title(ylab="Oxygen Concentration (%)", xlab="Time (hours)")
axis(1)
axis(2)
std <- read.csv(file='std.csv', sep=',', header=FALSE)
for ( i in seq(1,length( std ),1) ) arrows(A, oxygen_average[,i]-std[,i], A, oxygen_average[,i]+std[,i], length=0.05, angle=90, code=3)
arrows(A, oxygen_average$V1-std$V1, A, oxygen_average$V1+std$V1, length=0.0, angle=90, code=3)
